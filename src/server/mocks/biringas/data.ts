function build(seed: MockSeed): BiringaListing {
  return {
    id: seed.id,
    slug: seed.slug,
    name: seed.name,
    age: seed.age,
    city: seed.city,
    neighborhood: seed.neighborhood,
    pricePerHour: seed.pricePerHour,
    mainImage: pick(seed.imageIndex),
    gallery: seed.galleryIndices.map(pick),
    verified: true,
    hasVideo: seed.hasVideo,
    hasAudio: seed.hasAudio,
    tags: seed.tags,
    bio: seed.bio,
    shortBio: seed.shortBio,
    category: seed.category,
    sex: "mujeres",
    attention: seed.attention,
    contactChannels: seed.contactChannels,
    paymentByCard: seed.paymentByCard,
    faceVisible: seed.faceVisible,
    availableNow: seed.availableNow,
    storyAt: seed.storyAt,
    privatePhone: seed.privatePhone,
    privateWhatsapp: seed.privateWhatsapp,
    reputation: seed.reputation,
    attributes: seed.attributes,
    services: seed.services,
    specialServices: seed.specialServices,
    meetingContexts: seed.meetingContexts,
    coords: jitter(cityCoords[seed.city], Number(seed.id)),
    createdAt: seed.createdAt,
    updatedAt: seed.updatedAt,
  };
}

