function jitter(base: { lat: number; lng: number }, seed: number) {
  const offset = ((seed * 9301 + 49297) % 233280) / 233280 - 0.5;
  return { lat: base.lat + offset * 0.02, lng: base.lng + offset * 0.02 };
}

function pick(index: number): string {
  return IMAGE_POOL[index % IMAGE_POOL.length];
}

